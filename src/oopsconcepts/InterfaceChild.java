package oopsconcepts;

public class InterfaceChild implements InterfaceExample {

	@Override
	public void display() {
	System.out.println("Inside display method");
		
	}

	@Override
	public void show() {
		System.out.println("Inside show method");
		
	}

	@Override
	public void view() {
		System.out.println("Inside view method");
		
	}

}
